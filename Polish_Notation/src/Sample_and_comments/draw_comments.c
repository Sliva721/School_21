char **calloc_matrix(int height, int width) {
    // Функция для выделения памяти под двумерный массив (матрицу) символов.
    char **matrix; // Объявление указателя на указатель для матрицы.
    
    // Выделение памяти для строк матрицы. Каждая строка будет указателем на массив символов.
    matrix = (char **)calloc(height, sizeof(char *));
    
    // Выделение памяти для каждой строки матрицы.
    for (int y = 0; y < height; y++) 
        matrix[y] = (char *)calloc(width, sizeof(char));
    
    return matrix; // Возвращение указателя на созданную матрицу.
}

