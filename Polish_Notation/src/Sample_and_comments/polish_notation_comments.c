
// ### Объяснение работы кода:
// 1. Код запускает программу, которая считывает строку ввода, ожидая, что пользователь введет выражение в польской нотации.
// 2. Строка передается функции `parsing_in_polish_notation`, которая, вероятно, преобразует это выражение и выводит результат в `output_string`.
// 3. Очищенный вводный буфер может быть лишним в этом контексте, так как его использование не всегда гарантирует ожидаемое поведение (можно его удалить).
// 4. Затем инициализируется структура данных для управления стеком, который может использоваться в последующих вычислениях.
// 5. Значение `5` сохраняется в переменной `x`, которая передается в функцию `counter`. Эта функция, вероятно, производит вычисления на основе результата предыдущего парсинга и использует значение `x`.
// 6. Результат вычисления выводится на экран.

#include <stdio.h>              // Подключение библиотеки для ввода-вывода.
#include "calculation.h"       // Подключение заголовочного файла для расчетов.
#include "mystack.h"           // Подключение заголовочного файла для работы с стеком.

int main(void) {               // Основная функция, начиная с нее выполняется программа.
    struct st *operations = NULL; // Указатель на структуру, которая будет хранить операции (инициализирован в NULL).
    char input_string[80], output_string[80]; // Массивы символов для ввода и вывода строк, размером 80 символов.

    fflush(stdin);            // Очистка буфера ввода (на практике это может не всегда работать корректно).
    fgets(input_string, 80, stdin); // Считывание строки с клавиатуры, максимум 79 символов + '\0'.

    // Вызов функции, которая парсит входную строку в польской нотации и заполняет выходную строку.
    parsing_in_polish_notation(operations, input_string, output_string);
    
    // Выводим результат после обработки строки с польской нотацией.
    printf("\n%s\n", output_string); 
    
    fflush(stdin);            // Очистка буфера ввода еще раз.
    
    HEAD head;               // Объявление переменной типа HEAD, предположительно для управления стеком операций.
    init(&head);             // Инициализация структуры (возможно, создаем пустой стек).

    int x = 5;               // Объявление и инициализация переменной x, которую будем использовать в расчетах.
    double y = counter(output_string, &head, x); // Вызов функции counter для выполнения расчетов на основе выходной строки.

    // Выводим результат расчетов в формате с плавающей точкой.
    printf("%lf", y);

    return 0;                // Возврат 0, означающий успешное завершение программы.
}